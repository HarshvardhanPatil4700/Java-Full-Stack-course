public class DataTypes {
    public static void main(String[] args) {
        // Primitive D.T : int, float, char, boolean
        int n = 10;
        byte b = 7;
        // byte b = 129; error:possible lossy conversion from int to byte (out of range)
        short s = 300;
        long l = 62884935902L; // long has a suffix as 'L' or 'l' , else it gives error

        float marks = 90.5F; // float has a suffix as 'f' or 'F' , else it gives error
        double price = 90.5; // default value for floating point values

        char c = 'a'; // Java follows Unicode for its char data type.
        char c1 = 0;
        // char c2 = 'ab'; OR char c2 ="a"; OR char c2 = ab; ERROR  

        boolean b1 = true;






    }
}
